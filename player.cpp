void player_init(void) {
  player.y = PLAYER_INIT_Y;
  player.x = PLAYER_INIT_Y;
  player.height = PLAYER_SIZE;
  player.missile_length = MISSILE_LENGTH;
  player.player_has_missile = 1;
  missile.box.bottomRight = {0, 0};
  missile.box.topLeft = {0, 0};
  player.lives = 3;
}

// player_draw in main loop
void player_moveUp(void) {
  if (player.y - 1 >= 0)
    player.y-=2;
}

void player_moveDown(void) {
  if (player.y + 1 < 128)
    player.y+=2;
}

void player_moveLeft(void) {
  if (player.x - 1 >= 0)
    player.x-=2;
}

void player_moveRight(void) {
  if (player.x + 1 < 128)
    player.x+=2;
}

void player_throw(void) {
  if (!player.player_has_missile)
    return;
  player.player_has_missile = 0;
  missile.box.topLeft = {player.x - MISSILE_WIDTH / 2,
                         player.y - MISSILE_LENGTH / 2};
  missile.box.bottomRight = {player.x + MISSILE_WIDTH / 2,
                             player.y + MISSILE_LENGTH / 2};
}

void player_missile_draw() {
  if (player_get_info().player_has_missile)
    return;
  uLCD.filled_rectangle(
      missile.box.topLeft.x + 5, missile.box.topLeft.y -= PLAYER_MISSILE_SPEED,
      missile.box.bottomRight.x + 5,
      missile.box.bottomRight.y -= PLAYER_MISSILE_SPEED, PLAYER_MISSILE_COLOR);
}
void player_missile_draw_blank() {
  if (player_get_info().player_has_missile)
    return;
  uLCD.filled_rectangle(missile.box.topLeft.x + 5, missile.box.topLeft.y,
                        missile.box.bottomRight.x + 5,
                        missile.box.bottomRight.y, 0);
}

void player_missile_return(void) {
  if (player_get_info().player_has_missile) {
    return;
  }
  player_missile_draw_blank();
  missile.box.topLeft = {0, 0};
  missile.box.bottomRight = {0, 0};
  player.player_has_missile = true;
  // printf("missile returned\n");
}

